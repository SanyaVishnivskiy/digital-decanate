package com.example.app.ui.createUser;

import android.widget.TextView;

public class CreateUserModelWithViews {
    public TextView getId() {
        return id;
    }

    public void setId(TextView id) {
        this.id = id;
    }

    public TextView getName() {
        return name;
    }

    public void setName(TextView name) {
        this.name = name;
    }

    public TextView getLastName() {
        return lastName;
    }

    public void setLastName(TextView lastName) {
        this.lastName = lastName;
    }

    public TextView getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(TextView phoneNumber) {
        this.phoneNumber = phoneNumber;
    }

    public TextView getEmail() {
        return email;
    }

    public void setEmail(TextView email) {
        this.email = email;
    }

    public TextView getStudentTicket() {
        return studentTicket;
    }

    public void setStudentTicket(TextView studentTicket) {
        this.studentTicket = studentTicket;
    }

    public TextView getGroupName() {
        return groupName;
    }

    public void setGroupName(TextView groupName) {
        this.groupName = groupName;
    }

    public TextView getFaculty() {
        return faculty;
    }

    public void setFaculty(TextView faculty) {
        this.faculty = faculty;
    }

    public TextView getCourse() {
        return course;
    }

    public void setCourse(TextView course) {
        this.course = course;
    }

    public TextView getRole() {
        return role;
    }

    public void setRole(TextView role) {
        this.role = role;
    }

    public TextView getPassword() {
        return password;
    }

    public void setPassword(TextView password) {
        this.password = password;
    }

    private TextView id;
    private TextView name;
    private TextView lastName;
    private TextView phoneNumber;
    private TextView email;
    private TextView studentTicket;
    private TextView groupName;
    private TextView faculty;
    private TextView course;
    private TextView role;
    private TextView password;
}
